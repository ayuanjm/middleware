class Solution {
    public int minSubarray(int[] nums, int p) {
        HashMap<Long, Integer> hashMap = new HashMap<>(16);
        long sum = 0, remainder = 0;
        int ans = Integer.MAX_VALUE;
        for (int x : nums) {
            sum += x;
        }
        remainder = sum % p;
        if (remainder == 0) {
            return 0;
        }
        sum = 0;
        long dummy = 0;
        hashMap.put(0L, -1);
        for (int i = 0; i < nums.length; ++i) {
            sum += nums[i];
            dummy = sum % p;
            long target = (dummy - remainder + p) % p;
            if (hashMap.containsKey(target)) {
                ans = ans > (i - hashMap.get(target)) ? (i - hashMap.get(target)) : ans;
            }
            hashMap.put(dummy, i);
        }
        return ans == nums.length ? -1 : ans;
    }
}
